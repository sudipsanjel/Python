#ARTHMETIC OPERATOR
#+,-.*,/,%**,//
#Example
x = 100
y = 10
print(x + y) #this is addition operator
print(x - y) #this is subtraction operator
print(x * y) #this is multiplication operator
print(x / y) #this is division operator
print(x % y) #this is modulus operator #it is remainder while division
print(x ** y) #this is exponential operator
print(x // y) #this is floor division operator 
#floor division round the result to the nearest whole number

